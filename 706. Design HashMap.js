class Node {
  constructor(key, value) {
    this.key = key
    this.value = value
    this.next = null
  }
}

class MyHashMap {
  constructor() {
    this.tableSize = 50021
    this.table = new Array(this.tableSize)
    this.factor = 2341
  }

  printTable() {
    console.log(this.table)
  }

  hashSum(key) {
    return (key * this.factor) % this.tableSize
  }

  put(key, value) {
    const idx = this.hashSum(key)

    if (this.table[idx]) {
      let prev = null
      let pointer = this.table[idx]
      while (pointer) {
        if (pointer.key === key) {
          pointer.value = value
          return
        }
        prev = pointer
        pointer = pointer.next
      }

      prev.next = new Node(key, value)
    } else {
      this.table[idx] = new Node(key, value)
    }
  }

  get(key) {
    const idx = this.hashSum(key)
    if (!this.table[idx]) return -1

    let pointer = this.table[idx]
    while (pointer !== null) {
      if (pointer.key === key) {
        return pointer.value
      }
      pointer = pointer.next
    }
    return -1
  }

  remove(key) {
    const idx = this.hashSum(key)
    if (!this.table[idx]) {
      return
    }

    let pointer = this.table[idx]
    if (pointer.key === key) this.table[idx] = pointer.next

    let prev = pointer
    pointer = pointer.next
    while (pointer !== null) {
      if (pointer.key === key) {
        prev.next = pointer.next
        return
      }

      prev = pointer
      pointer = pointer.next
    }
  }
}

